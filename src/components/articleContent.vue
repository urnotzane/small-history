<script>
import topBack from './topBack'
import axios from 'axios'

export default {
    name: "articleContent",
    components: {
        topBack
    },
    data: function () {
        return {
            articleContent: ""
        }
    },
    mounted: function () {
        
    },
    watch: {

    },
    activated: function () {
        this.promiseFun()
    },
    methods: {
        getContent: function(url) {
            const href = this.$route.query.href
            const that = this
            return new Promise (function (resolve, reject) {
                axios.get(url,{
                    params: {
                        _url: href
                    }
                })
                    .then(function (res) {
                    if(res.err) {
                        reject(res.err);
                        return;
                    }
                    resolve(res.data);

                    })
                    .catch(function (err) {
                console.log(err)
                })
            })
        },
        promiseFun: function () {
            const that = this;
            let promise = that.getContent('/api/article_content');
            promise.then(function (contents) {
                // console.log(contents.length);
                that.articleContent = contents; 
            })
            .then(function() {
                that.getSrc()
            })
            .then( function () {
                that.makeBetter()
            })
            .catch(err => {
                console.err(err)
            })
        },
        getSrc: function () {
            var imgs = document.getElementsByTagName("img");
            // console.log(imgs)
            // console.log(imgs.length)
            for(let i=2; i < imgs.length; i++) {
                let src = imgs[i].dataset.original
                imgs[i].setAttribute("src",src)
                imgs[i].style.maxWidth = "100%";
                imgs[i].style.padding = "10px 0";
            }
        } ,
        makeBetter: function () {
            try {
                const title = document.getElementById("news_template_03_titleContent"),
                source = document.getElementById("news_template_03_AuthorAndTime"),
                surplus = document.getElementById("ID_disclaimer");

                title.style.fontSize = "20px"
                title.style.fontWeight = "bold"
                title.style.padding = "3px 0"
                source.style.fontSize = "12px"
                source.style.color = "#888"
                source.style.marginBottom = "10px"
                surplus.style.display = "none"
            } catch (error) {
                console.log("Cannot read property 'style' of null!文档未加载完或不存在该属性时而出现的无关紧要的错误。")
            }
            
        }
    }
}
</script>
